    private void defaults() {
        this.tableModel.addColumn("ID");
        this.tableModel.addColumn("NOMBRE");
        this.tableModel.addColumn("DESCRIPCIÃ“N");
        this.tableModel.addColumn("PRECIO");

        this.products.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        this.setResizable(false);
        this.setTitle("Productos");
    }

    private void setListener() {
        edit.addActionListener(controller);
        delete.addActionListener(controller);
        add.addActionListener(controller);
        products.addMouseListener(controller);
        refresh.addActionListener(controller);
    }

    public void updateProducts() {
        tableModel.setRowCount(0);

        for (Product product : model.all()) {
            Object[] row = new Object[4];
            row[0] = product.getId();
            row[1] = product.getName();
            row[2] = product.getDescription();
            row[3] = product.getPrice();
            tableModel.addRow(row);
        }

        products.setModel(tableModel);
    }

    public void open() {
        this.setLocationRelativeTo(null);
        this.setVisible(true);
    }

    public void close() {
        this.dispose();
    }

